/**
 * @fileoverview Компонент отображения ошибок
 * 
 * Этот компонент представляет универсальный компонент для отображения ошибок.
 * Используется во всех частях приложения для показа сообщений об ошибках.
 * 
 * Функциональность:
 * - Отображение сообщения об ошибке в стилизованном блоке
 * - Использование стандартных CSS классов для ошибок
 * - Центрированное расположение на странице
 * - Универсальное использование в любом контексте
 * 
 * Стили:
 * - Использует CSS классы alert и alert-danger
 * - Красный цвет для выделения ошибки
 * - Жирный шрифт для слова "Ошибка:"
 * - Полноширинный контейнер страницы
 * 
 * Использование:
 * - При ошибках загрузки данных
 * - При ошибках валидации форм
 * - При сетевых ошибках
 * - При ошибках авторизации
 * 
 * @param {string} error - текст сообщения об ошибке
 * @author SYRUP CHAT Team
 * @version 1.0.0
 */

import React from 'react'

const ErrorMessage = ({ error }) => {
  return (
    <div className="page-container">
      <div className="alert alert-danger">
        <strong>Ошибка:</strong> {error}
      </div>
    </div>
  )
}

export default ErrorMessage 
